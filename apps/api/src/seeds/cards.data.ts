export interface CardSeedData {
  cardId: number;
  name: string;
  heavenlyStem: string;
  earthlyBranch: string;
  wuXingElement: string;
  nayin: string;
  description: string;
  sortOrder: number;
}

export function buildCardsData(): CardSeedData[] {
  const cards: CardSeedData[] = [];
  let cardId = 1;

  for (let ganIdx = 0; ganIdx < 60; ganIdx++) {
    const gan = GAN[ganIdx % 10];
    const zhi = ZHI[ganIdx % 12];
    const name = `${gan}${zhi}`;
    const nayinIdx = Math.floor(ganIdx / 2);
    const nayin = NAYIN_LIST[nayinIdx] ?? '';
    const wuXingElement = `${GAN_WUXING[gan]}${ZHI_WUXING[zhi]}`;

    cards.push({
      cardId,
      name,
      heavenlyStem: gan,
      earthlyBranch: zhi,
      wuXingElement,
      nayin,
      description: CARD_CENTRAL_MAP[name] ?? '',
      sortOrder: cardId,
    });
    cardId++;
  }

  return cards;
}
